package by.schepov.xmlparser.exception;

public class GrowingTipBuilderException extends Exception {
    public GrowingTipBuilderException() {
    }

    public GrowingTipBuilderException(String message) {
        super(message);
    }

    public GrowingTipBuilderException(String message, Throwable cause) {
        super(message, cause);
    }

    public GrowingTipBuilderException(Throwable cause) {
        super(cause);
    }
}
