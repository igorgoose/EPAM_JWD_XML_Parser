package by.schepov.xmlparser.exception;

public class FlowerBuilderException extends Exception {
    public FlowerBuilderException() {
    }

    public FlowerBuilderException(String message) {
        super(message);
    }

    public FlowerBuilderException(String message, Throwable cause) {
        super(message, cause);
    }

    public FlowerBuilderException(Throwable cause) {
        super(cause);
    }
}
