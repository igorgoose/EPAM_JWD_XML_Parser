package by.schepov.xmlparser.exception;

public class FlowerDispatcherException extends Exception {
    public FlowerDispatcherException() {
    }

    public FlowerDispatcherException(String message) {
        super(message);
    }

    public FlowerDispatcherException(String message, Throwable cause) {
        super(message, cause);
    }

    public FlowerDispatcherException(Throwable cause) {
        super(cause);
    }
}
